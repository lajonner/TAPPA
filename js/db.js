/***INICIALIZACION DE CLASE DE OBJETOS EN BASE DE DATOS***/
    function objectTableDB(name,columns){
        this.name=name;
        this.columns = columns;
        this.columns["id"]="INTEGER PRIMARY KEY AUTOINCREMENT";
    }

    objectTableDB.prototype.createQuery = function() {
        var columnDescriptions="";
        for (var eachColumn in this.columns){
            if (eachColumn!="id"){
                columnDescriptions=columnDescriptions.concat(",",eachColumn," "+this.columns[eachColumn]);
            }
        }
        var CREATE = "CREATE TABLE IF NOT EXISTS "+this.name+" (id INTEGER PRIMARY KEY AUTOINCREMENT"+columnDescriptions+")";
        return CREATE;
    }


/***CLASE DB CONEXION ABSTRACTA A LA BASE DE DATOS***/
    function DB(name,version,description,size){
        this.name=name;
        this.version=version;
        this.description=description;
        this.size=size;
        this.MODELS={};
        this.CREATE_TABLES=[];
        this.db=null;
    }

    DB.prototype.registerObjectDB=function(name,columns){
        var objDB = new objectTableDB(name,columns);
        this.CREATE_TABLES.push(name);
        this.MODELS[name]=objDB
    }

    DB.prototype.initializeDB=function() {
        if(this.db==null){
           this.db = window.openDatabase(DATABASE_NAME, DATABASE_VERSION, DATABASE_DESCRIPTION, DATABASE_SIZE);
           this.createDataBase();
        }
    }

    DB.prototype.createDataBase=function() {
       var ALL_CREATE_TABLES=this.CREATE_TABLES;
       var ALL_OBJECT_TABLES=this.MODELS;
       this.db.transaction(function(tx) {
            for (var index in ALL_CREATE_TABLES) {
                var objDB=(ALL_OBJECT_TABLES[ALL_CREATE_TABLES[index]]);
                tx.executeSql(objDB.createQuery());           
            } 
       });       
    }

    DB.prototype.insert=function(model,values) {
       this.db.transaction(function(tx) {
            var sql="INSERT INTO "+model+"(";
            var arrayValues=[];
            var valuesPos="*";
            var valuesColumns="*";
            for (var each_value in values) {
                arrayValues.push(values[each_value]);  
                valuesPos=valuesPos+",?";
                valuesColumns=valuesColumns+","+each_value;
            } 
            valuesPos=valuesPos.replace("*,","").replace("*","")
            valuesColumns=valuesColumns.replace("*,","").replace("*","")
            sql=sql+valuesColumns+")values("+valuesPos+")";
            tx.executeSql(sql,arrayValues); 
       },this.errorDataBase);       
    }

    DB.prototype.update=function(model,id,values) {
       this.db.transaction(function(tx) {
            var sql="UPDATE "+model+" SET ";
            var arrayValues=[];
            var valuesPos="*";
            for (var each_value in values) {
                arrayValues.push(values[each_value]);  
                valuesPos=valuesPos+","+each_value+"=?";
            } 
            valuesPos=valuesPos.replace("*,","").replace("*","")
            sql=sql+valuesPos+" WHERE ID=?";
            arrayValues.push(id);
            tx.executeSql(sql,arrayValues); 
       },this.errorDataBase);       
    }

DB.prototype.unlink=function(model,id) {
       this.db.transaction(function(tx) {
            var sql="delete from "+model+" where id=?";
            tx.executeSql(sql,[id]); 
       },this.errorDataBase);       
    }

     DB.prototype.executeSql=function(txFX,errorFx) {
        this.db.transaction(txFX,errorFx);
    }

    DB.prototype.errorDataBase=function(err) {
        alert("Error processing SQL("+err.code+"): "+err.message);
    }

